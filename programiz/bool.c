#include <stdio.h>

/*
    Comparison operator:
    - (>, <, >=, <=, ==, !=)

    Logical operator:
    - AND (&&)
    - OR (||)
    - NOT (!) 
*/

int main(){
    // True is represent by 1
    int cek1 = (3 > 2);
    printf("%d ", cek1);

    // False is represent by 0
    int cek2 = (3 < 2);
    printf("%d ", cek2);

    return 0;
}